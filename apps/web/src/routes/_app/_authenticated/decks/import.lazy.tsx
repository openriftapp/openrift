function DeckImportPage() {
  const { replaceDeckId } = Route.useSearch();
  const { allPrintings } = useCards();
  const { zoneOrder, zoneLabels } = useZoneOrder();
  const createDeck = useCreateDeck();
  const saveDeckCards = useSaveDeckCards();
  const navigate = useNavigate();

  const replaceDeckQuery = useQuery({
    ...deckDetailQueryOptions(replaceDeckId ?? ""),
    enabled: Boolean(replaceDeckId),
  });
  const replaceDeckName = replaceDeckQuery.data?.deck.name;
  const isReplaceMode = Boolean(replaceDeckId);

  const [step, setStep] = useState<ImportStep>("input");
  const [rawText, setRawText] = useState("");
  const [importFormat, setImportFormat] = useState<DeckImportFormat>("piltover");
  const [deckName, setDeckName] = useState("Imported Deck");
  const [deckFormat, setDeckFormat] = useState<DeckFormat>("constructed");
  const [matchedEntries, setMatchedEntries] = useState<DeckMatchedEntry[]>([]);
  const [parseWarnings, setParseWarnings] = useState<string[]>([]);
  const [skippedIndices, setSkippedIndices] = useState<Set<number>>(new Set());
  const [expandedIndices, setExpandedIndices] = useState<Set<number>>(new Set());
  const [isImporting, setIsImporting] = useState(false);
  const [confirmReplaceOpen, setConfirmReplaceOpen] = useState(false);

  const handleParse = (text: string) => {
    const { entries, warnings } = parseDeckImportData(text, importFormat);
    setParseWarnings(warnings);

    if (entries.length === 0) {
      return;
    }

    const matched = matchDeckEntries(entries, allPrintings);
    const zoneIndex = Object.fromEntries(zoneOrder.map((slug, index) => [slug, index]));
    const sorted = matched.toSorted((entryA, entryB) => {
      const zoneDiff = (zoneIndex[entryA.zone] ?? 99) - (zoneIndex[entryB.zone] ?? 99);
      if (zoneDiff !== 0) {
        return zoneDiff;
      }
      const nameA = entryDisplayName(entryA);
      const nameB = entryDisplayName(entryB);
      if (nameA !== nameB) {
        return nameA.localeCompare(nameB);
      }
      return STATUS_SORT_ORDER[entryA.status] - STATUS_SORT_ORDER[entryB.status];
    });
    setMatchedEntries(sorted);
    setSkippedIndices(new Set());

    // Auto-expand non-exact entries so the user sees what needs attention
    const nonExact = new Set<number>();
    for (let index = 0; index < sorted.length; index++) {
      if (sorted[index].status !== "exact") {
        nonExact.add(index);
      }
    }
    setExpandedIndices(nonExact);

    setStep("preview");
  };

  const handleResolve = (index: number, card: ResolvedCard) => {
    setMatchedEntries((prev) =>
      prev.map((entry, entryIndex) =>
        entryIndex === index
          ? {
              ...entry,
              resolvedCard: card,
              status: "exact" as DeckMatchStatus,
              zone: entry.entry.explicitZone ?? entry.zone,
            }
          : entry,
      ),
    );
  };

  const handleZoneChange = (index: number, zone: DeckZone) => {
    setMatchedEntries((prev) =>
      prev.map((entry, entryIndex) => (entryIndex === index ? { ...entry, zone } : entry)),
    );
  };

  const handleSkip = (index: number) => {
    setSkippedIndices((prev) => new Set([...prev, index]));
  };

  const handleUnskip = (index: number) => {
    setSkippedIndices((prev) => {
      const next = new Set(prev);
      next.delete(index);
      return next;
    });
  };

  const handleToggleExpand = (index: number) => {
    setExpandedIndices((prev) => {
      const next = new Set(prev);
      if (next.has(index)) {
        next.delete(index);
      } else {
        next.add(index);
      }
      return next;
    });
  };

  const readyEntries = matchedEntries.filter(
    (entry, index) => entry.resolvedCard && !skippedIndices.has(index),
  );
  const needsAttention = matchedEntries.filter(
    (entry, index) => !entry.resolvedCard && !skippedIndices.has(index),
  );
  const skippedCount = skippedIndices.size;
  const totalCards = readyEntries.reduce((sum, entry) => sum + entry.entry.quantity, 0);

  const buildCards = () => {
    // Group by cardId + zone + preferredPrintingId, summing quantities.
    // Printing-specific matches (piltover/tts short codes) become distinct
    // rows from any default-art rows of the same card.
    const cardMap = new Map<
      string,
      { cardId: string; zone: DeckZone; quantity: number; preferredPrintingId: string | null }
    >();
    for (const entry of readyEntries) {
      if (!entry.resolvedCard) {
        continue;
      }
      const preferredPrintingId = entry.resolvedCard.preferredPrintingId;
      const key = `${entry.resolvedCard.cardId}::${entry.zone}::${preferredPrintingId ?? ""}`;
      const existing = cardMap.get(key);
      if (existing) {
        existing.quantity += entry.entry.quantity;
      } else {
        cardMap.set(key, {
          cardId: entry.resolvedCard.cardId,
          zone: entry.zone,
          quantity: entry.entry.quantity,
          preferredPrintingId,
        });
      }
    }
    return [...cardMap.values()];
  };

  const executeReplace = () => {
    if (!replaceDeckId) {
      return;
    }
    const targetName = replaceDeckName ?? "deck";
    setIsImporting(true);
    saveDeckCards.mutate(
      { deckId: replaceDeckId, cards: buildCards() },
      {
        onSuccess: () => {
          toast.success(`Replaced cards in "${targetName}" with ${totalCards} cards.`);
          void navigate({ to: "/decks/$deckId", params: { deckId: replaceDeckId } });
        },
        onError: () => {
          toast.error("Failed to replace deck cards.");
          setIsImporting(false);
        },
      },
    );
  };

  const executeCreate = () => {
    const trimmedName = deckName.trim();
    if (!trimmedName) {
      toast.error("Please enter a deck name.");
      return;
    }

    setIsImporting(true);
    const cards = buildCards();

    createDeck.mutate(
      { name: trimmedName, format: deckFormat },
      {
        onSuccess: (data) => {
          const deck = data as DeckResponse;
          saveDeckCards.mutate(
            { deckId: deck.id, cards },
            {
              onSuccess: () => {
                toast.success(`Imported deck "${trimmedName}" with ${totalCards} cards.`);
                void navigate({ to: "/decks/$deckId", params: { deckId: deck.id } });
              },
              onError: () => {
                toast.error("Failed to save deck cards.");
                setIsImporting(false);
              },
            },
          );
        },
        onError: () => {
          toast.error("Failed to create deck.");
          setIsImporting(false);
        },
      },
    );
  };

  const handleImport = () => {
    if (isReplaceMode) {
      setConfirmReplaceOpen(true);
      return;
    }
    executeCreate();
  };

  if (step === "input") {
    return (
      <InputStep
        rawText={rawText}
        onTextChange={setRawText}
        importFormat={importFormat}
        onImportFormatChange={setImportFormat}
        onParse={handleParse}
        parseWarnings={parseWarnings}
        replaceDeckName={isReplaceMode ? (replaceDeckName ?? "") : undefined}
      />
    );
  }

  return (
    <>
      <PreviewStep
        matchedEntries={matchedEntries}
        allPrintings={allPrintings}
        parseWarnings={parseWarnings}
        skippedIndices={skippedIndices}
        expandedIndices={expandedIndices}
        deckName={deckName}
        deckFormat={deckFormat}
        zoneOrder={zoneOrder}
        zoneLabels={zoneLabels}
        readyCount={readyEntries.length}
        needsAttentionCount={needsAttention.length}
        skippedCount={skippedCount}
        totalCards={totalCards}
        isImporting={isImporting}
        replaceDeckName={isReplaceMode ? (replaceDeckName ?? "") : undefined}
        onResolve={handleResolve}
        onZoneChange={handleZoneChange}
        onSkip={handleSkip}
        onUnskip={handleUnskip}
        onToggleExpand={handleToggleExpand}
        onDeckNameChange={setDeckName}
        onDeckFormatChange={setDeckFormat}
        onImport={handleImport}
        onBack={() => setStep("input")}
      />
      <AlertDialog open={confirmReplaceOpen} onOpenChange={setConfirmReplaceOpen}>
        <AlertDialogContent>
          <AlertDialogHeader>
            <AlertDialogTitle>Replace deck contents?</AlertDialogTitle>
            <AlertDialogDescription>
              This will remove all existing cards in &ldquo;{replaceDeckName ?? "this deck"}&rdquo;
              and replace them with the {totalCards} imported {totalCards === 1 ? "card" : "cards"}.
              The deck&apos;s name and format are kept.
            </AlertDialogDescription>
          </AlertDialogHeader>
          <AlertDialogFooter>
            <AlertDialogCancel>Cancel</AlertDialogCancel>
            <AlertDialogAction
              variant="destructive"
              onClick={() => {
                setConfirmReplaceOpen(false);
                executeReplace();
              }}
            >
              Replace
            </AlertDialogAction>
          </AlertDialogFooter>
        </AlertDialogContent>
      </AlertDialog>
    </>
  );
}

