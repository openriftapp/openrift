function PreviewStep({
  matchedEntries,
  allPrintings,
  parseWarnings,
  skippedIndices,
  expandedIndices,
  deckName,
  deckFormat,
  zoneOrder,
  zoneLabels,
  readyCount,
  needsAttentionCount,
  skippedCount,
  totalCards,
  isImporting,
  replaceDeckName,
  onResolve,
  onZoneChange,
  onSkip,
  onUnskip,
  onToggleExpand,
  onDeckNameChange,
  onDeckFormatChange,
  onImport,
  onBack,
}: {
  matchedEntries: DeckMatchedEntry[];
  allPrintings: Printing[];
  parseWarnings: string[];
  skippedIndices: Set<number>;
  expandedIndices: Set<number>;
  deckName: string;
  deckFormat: DeckFormat;
  zoneOrder: DeckZone[];
  zoneLabels: Record<DeckZone, string>;
  readyCount: number;
  needsAttentionCount: number;
  skippedCount: number;
  totalCards: number;
  isImporting: boolean;
  replaceDeckName?: string;
  onResolve: (index: number, card: ResolvedCard) => void;
  onZoneChange: (index: number, zone: DeckZone) => void;
  onSkip: (index: number) => void;
  onUnskip: (index: number) => void;
  onToggleExpand: (index: number) => void;
  onDeckNameChange: (name: string) => void;
  onDeckFormatChange: (format: DeckFormat) => void;
  onImport: () => void;
  onBack: () => void;
}) {
  const isReplaceMode = replaceDeckName !== undefined;
  const canImport = readyCount > 0 && (isReplaceMode || deckName.trim().length > 0);

  return (
    <div className="mx-auto max-w-3xl space-y-4 px-4 py-6">
      <div className="flex items-center justify-between">
        <div>
          <h2 className="text-lg font-semibold">
            {isReplaceMode ? "Replace Preview" : "Import Preview"}
          </h2>
          <p className="text-muted-foreground text-sm">
            {matchedEntries.length} card{matchedEntries.length === 1 ? "" : "s"} parsed
            {isReplaceMode && replaceDeckName ? (
              <>
                {" "}
                — replacing contents of{" "}
                <strong className="text-foreground">&ldquo;{replaceDeckName}&rdquo;</strong>
              </>
            ) : null}
          </p>
        </div>
        <Button variant="outline" size="sm" onClick={onBack}>
          Back
        </Button>
      </div>

      {/* Entry list */}
      <div className="divide-border divide-y rounded-md border">
        {matchedEntries.map((entry, index) => (
          <DeckImportEntryRow
            key={`${entry.entry.shortCode ?? entry.entry.cardName ?? ""}-${entry.zone}-${index}`}
            entry={entry}
            allPrintings={allPrintings}
            index={index}
            zoneOrder={zoneOrder}
            zoneLabels={zoneLabels}
            isSkipped={skippedIndices.has(index)}
            isExpanded={expandedIndices.has(index)}
            onResolve={onResolve}
            onZoneChange={onZoneChange}
            onSkip={onSkip}
            onUnskip={onUnskip}
            onToggleExpand={onToggleExpand}
          />
        ))}
      </div>

      {/* Parse warnings */}
      {parseWarnings.length > 0 && (
        <div className="rounded-md border border-amber-200 bg-amber-50 dark:border-amber-900 dark:bg-amber-950">
          <p className="px-3 py-2 font-medium text-amber-800 dark:text-amber-300">
            {parseWarnings.length} warning{parseWarnings.length === 1 ? "" : "s"} while parsing
          </p>
          <div className="space-y-1 border-t border-amber-200 px-3 py-2 dark:border-amber-900">
            {parseWarnings.map((warning) => (
              <p key={warning} className="text-amber-700 dark:text-amber-400">
                {warning}
              </p>
            ))}
          </div>
        </div>
      )}

      {/* Summary + deck options + import button */}
      <div className="bg-muted/50 space-y-4 rounded-md border p-4">
        <div className="flex flex-wrap items-center gap-2 text-sm">
          <Badge variant="secondary">{readyCount} ready</Badge>
          {needsAttentionCount > 0 && (
            <Badge variant="default">{needsAttentionCount} need attention</Badge>
          )}
          {skippedCount > 0 && <Badge variant="ghost">{skippedCount} skipped</Badge>}
        </div>

        {needsAttentionCount > 0 && (
          <p className="text-muted-foreground text-sm">
            Having trouble importing?{" "}
            <a
              href="https://github.com/eikowagenknecht/openrift/issues"
              target="_blank"
              rel="noreferrer"
              className="text-foreground underline"
            >
              Open a GitHub issue
            </a>{" "}
            and we&apos;ll take a look.
          </p>
        )}

        <div className="flex flex-wrap items-end gap-3">
          {!isReplaceMode && (
            <>
              <div className="space-y-1.5">
                <Label htmlFor="preview-deck-name">Deck name</Label>
                <Input
                  id="preview-deck-name"
                  value={deckName}
                  onChange={(event) => onDeckNameChange(event.target.value)}
                  className="w-[200px]"
                />
              </div>

              <div className="space-y-1.5">
                <Label htmlFor="preview-deck-format">Format</Label>
                <Select
                  value={deckFormat}
                  onValueChange={(value) => onDeckFormatChange(value as DeckFormat)}
                >
                  <SelectTrigger id="preview-deck-format" className="mb-0 w-[140px]">
                    <SelectValue>
                      {(value: string) => DECK_FORMAT_LABELS[value] ?? value}
                    </SelectValue>
                  </SelectTrigger>
                  <SelectContent>
                    <SelectItem value="constructed">Constructed</SelectItem>
                    <SelectItem value="freeform">Freeform</SelectItem>
                  </SelectContent>
                </Select>
              </div>
            </>
          )}

          <Button
            variant={isReplaceMode ? "destructive" : "default"}
            onClick={onImport}
            disabled={!canImport || isImporting}
          >
            {isImporting ? (
              <>
                <Loader2Icon className="mr-2 size-4 animate-spin" />
                {isReplaceMode ? "Replacing..." : "Importing..."}
              </>
            ) : isReplaceMode ? (
              <>
                Replace with {totalCards} {totalCards === 1 ? "card" : "cards"}
              </>
            ) : (
              <>
                Import {totalCards} {totalCards === 1 ? "card" : "cards"}
              </>
            )}
          </Button>
          {needsAttentionCount > 0 && !isImporting && (
            <span className="text-muted-foreground text-sm">
              (skips {needsAttentionCount} unmatched)
            </span>
          )}
        </div>
      </div>
    </div>
  );
}

