function InputStep({
  rawText,
  onTextChange,
  importFormat,
  onImportFormatChange,
  onParse,
  parseWarnings,
  replaceDeckName,
}: {
  rawText: string;
  onTextChange: (text: string) => void;
  importFormat: DeckImportFormat;
  onImportFormatChange: (format: DeckImportFormat) => void;
  onParse: (text: string) => void;
  parseWarnings: string[];
  replaceDeckName?: string;
}) {
  const isReplaceMode = replaceDeckName !== undefined;
  return (
    <div className="mx-auto max-w-2xl space-y-6 px-4 py-6">
      <div>
        <h2 className="text-lg font-semibold">
          {isReplaceMode ? "Replace deck contents" : "Import Deck"}
        </h2>
        <p className="text-muted-foreground text-sm">
          {isReplaceMode ? (
            <>
              Paste a deck code, text list, or TTS string to replace the cards in
              {replaceDeckName ? (
                <>
                  {" "}
                  <strong className="text-foreground">&ldquo;{replaceDeckName}&rdquo;</strong>
                </>
              ) : (
                " this deck"
              )}
              . The deck&apos;s name and format are kept.
            </>
          ) : (
            <>
              Paste a deck code, text list, or TTS string to import a deck. Want another format
              supported?{" "}
              <a
                href="https://github.com/eikowagenknecht/openrift/issues"
                target="_blank"
                rel="noreferrer"
                className="text-foreground underline"
              >
                Open a GitHub issue
              </a>
              .
            </>
          )}
        </p>
      </div>

      <Tabs
        value={importFormat}
        onValueChange={(value) => onImportFormatChange(value as DeckImportFormat)}
      >
        <TabsList>
          <TabsTrigger value="piltover">Deck Code</TabsTrigger>
          <TabsTrigger value="text">Text</TabsTrigger>
          <TabsTrigger value="tts">TTS</TabsTrigger>
        </TabsList>

        <TabsContent value={importFormat}>
          <div className="space-y-3">
            <p className="text-muted-foreground text-sm">{IMPORT_DESCRIPTIONS[importFormat]}</p>
            <Textarea
              value={rawText}
              onChange={(event) => onTextChange(event.target.value)}
              placeholder={IMPORT_PLACEHOLDERS[importFormat]}
              className={cn(
                "font-mono text-xs",
                importFormat === "piltover" ? "min-h-[120px]" : "min-h-[320px]",
              )}
            />

            <div className="flex items-center gap-3">
              <Button onClick={() => onParse(rawText)} disabled={rawText.trim().length === 0}>
                <UploadIcon className="mr-2 size-4" />
                Parse
              </Button>
            </div>
          </div>
        </TabsContent>
      </Tabs>

      {parseWarnings.length > 0 && (
        <div className="rounded-md border border-red-200 bg-red-50 p-3 dark:border-red-900 dark:bg-red-950">
          {parseWarnings.map((warning) => (
            <p key={warning} className="text-sm text-red-700 dark:text-red-400">
              {warning}
            </p>
          ))}
        </div>
      )}
    </div>
  );
}

